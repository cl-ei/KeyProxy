    printf("R3: 0x%08X\n", r3);
    printf("R12: 0x%08X\n", r12);
    printf("LR: 0x%08X\n", lr);
    printf("PC: 0x%08X\n", pc);
    printf("PSR: 0x%08X\n", psr);

